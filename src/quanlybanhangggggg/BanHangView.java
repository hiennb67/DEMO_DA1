    private void btApDungMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btApDungMouseClicked
        // TODO add your handling code here:
        for (KhuyenMai khuyenMai : qlkm.getAllKM()) {
            if (txtKMBanHang.getText().equals(khuyenMai.getMa())) {
                if (Integer.parseInt(txtTongTien.getText()) <= Integer.parseInt(khuyenMai.getDieuKien())) {
                    JOptionPane.showMessageDialog(this, "Không được áp dụng mã");
                    return;
                }
            }
//            JOptionPane.showMessageDialog(this, "Được áp dụng mã");
        }
        for (KhuyenMai km : qlkm.getAllKM()) {
            if (txtKMBanHang.getText().equals(km.getMa())) {
                txtTongTien.setText(Integer.parseInt(txtTongTien.getText()) - Integer.parseInt(txtTongTien.getText()) * km.getGiam() / 100 + "");
                JOptionPane.showMessageDialog(this, "Áp dụng thành công");

                String ma = txtKMBanHang.getText();
                qlkm.truSoLuongKhuyenMai(ma);
                loadBangKhuyeMai();

                return;
            }
        }
        JOptionPane.showMessageDialog(this, "áp dụng thất bại");


    }//GEN-LAST:event_btApDungMouseClicked

