    private void tblGioHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGioHangMouseClicked
        // TODO add your handling code here:
        int a = (int) tblGioHang.getSelectedRow();
        String i = JOptionPane.showInputDialog("Thay đổi số lượng");
        if (i == null) {
            return;
        }
        int sl = (int) tblGioHang.getValueAt(a, 3);
        String ma = (String) tblGioHang.getValueAt(a, 1);
        int td = sl - Integer.parseInt(i);
        Boolean c = qlsp.congSP(td, ma);
        if (c) {
            loadBangSanPham();
        }
        Boolean tru = qlsp.truiSPtrogMa(Integer.parseInt(i), ma);
        if (tru) {
            loadGioHang();
        }
        int tong = 0;
        for (GioHang gioHang : qlgh.getGioHang(txtMa.getText())) {
            tong += gioHang.thanhTien();
        }
        txtTongTien.setText(tong + "");
    }//GEN-LAST:event_tblGioHangMouseClicked

