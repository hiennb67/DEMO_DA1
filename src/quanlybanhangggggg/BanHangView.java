    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:

        for (HoaDon h : qlhd.getHD()) {
            if (txtMa.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn hóa đơn");
                return;
            }
        }
        int i = tblSanPham.getSelectedRow();
        String ma = (String) tblSanPham.getValueAt(i, 0);
        String a = JOptionPane.showInputDialog("Vui lòng điền số lượng");
        if (a == null) {
            return;
        }
        int sl = (int) tblSanPham.getValueAt(i, 5);
        if (Integer.parseInt(a) > sl) {
            JOptionPane.showMessageDialog(this, "Nhập lại số lượng");
            return;
        }
        if (Integer.parseInt(a) <= 0) {
            JOptionPane.showMessageDialog(this, "Nhập lại số lượng");
            return;
        }
        Boolean Check1 = qlsp.truSP(Integer.parseInt(a), ma);
        if (Check1) {
            JOptionPane.showMessageDialog(this, "Thành công");
            loadBangSanPham();
        }

        String ten = (String) tblSanPham.getValueAt(i, 1);
        int donGia = (int) tblSanPham.getValueAt(i, 6);
        GioHang gh = new GioHang(ma, ten, Integer.parseInt(a), donGia);
        String maHD = txtMa.getText();
        for (GioHang gioHang : qlgh.getGioHang(maHD)) {
            if (ma.equals(gioHang.getMa())) {
                Boolean nhanDoi = qlsp.nhanDoiSPtrogMa(Integer.parseInt(a), ma);
                loadGioHang();
                return;
            }
        }
        Boolean check = qlgh.themGioHang(gh, maHD);
        loadGioHang();

        int tong = 0;
        for (GioHang gioHang : qlgh.getGioHang(maHD)) {
            tong += gioHang.thanhTien();
        }
        txtTongTien.setText(tong + "");
    }//GEN-LAST:event_tblSanPhamMouseClicked

